const sumar = function(a, b) {
    return a + b;
} // se declara la función sumar.
const restar = function(a, b) {
    return a - b;
} // se declara la función restar.
const multiplicar = function(a, b) {
    return a * b;
} // se declara la función multiplicar.
const dividir = function(a, b) {
    return a / b;
} // se declara la función dividir.
console.log("4 + 5 =", sumar(4, 5)); // se llama a la función sumar.
console.log("3 - 6 =", restar(3, 6)); // se llama a la función restar.
console.log("2 * 7 =", multiplicar(2, 7)); // se llama a la función multiplicar.
console.log("20 / 4 =", dividir(20, 4)); // se llama a la función dividir.